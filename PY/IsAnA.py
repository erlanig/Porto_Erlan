# Deskripsi : menghasilkan sebuah nilai boolean yang bernilai benar jika karakter
# tersebut adalah huruf 'A'
# Defisi dan spesifikasi dari fungsi IsAnA(c) adalah :
# IsAnA : integer --> boolean
#       IsAnA(c) benar jika c adalah karakter (huruf) 'A'

# Realisasi
def IsAnA(x) :
    return(x) == 'A'

# Aplikasi
print(IsAnA('1'))
print(IsAnA('0'))
print(IsAnA('-1'))
